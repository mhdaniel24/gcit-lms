package domain;

public class BookGenres {
	private int genreId;
	private int bookId;
	
	/**
	 * @return
	 */
	public int getGenreId() {
		return genreId;
	}
	/**
	 * @param genreId
	 */
	public void setGenreId(int genreId) {
		this.genreId = genreId;
	}
	/**
	 * @return
	 */
	public int getBookId() {
		return bookId;
	}
	/**
	 * @param bookId
	 */
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	
	
}
